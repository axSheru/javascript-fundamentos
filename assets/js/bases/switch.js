const dia = 2;

switch (dia) {// Esto compara con ===
    case 0:
        console.log( 'Domingo' );
        break;// Se utiliza para salir de la estructura de control.
    case 1:
        console.log( 'Lunes' );
        break;
    case 2:
        console.log( 'Martes' );
        break;
    default:
        console.log( 'No es domingo, lunes o martes' );
        break;
}


